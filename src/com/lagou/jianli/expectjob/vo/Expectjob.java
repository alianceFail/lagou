package com.lagou.jianli.expectjob.vo;

public class Expectjob {
	private int id;
	private String expectCity;
	private String type;
	private String expectPosition;
	private String expectSalary;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getexpectCity() {
		return expectCity;
	}
	public void setexpectCity(String expectCity) {
		this.expectCity = expectCity;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getExpectPosition() {
		return expectPosition;
	}
	public void setExpectPosition(String expectPosition) {
		this.expectPosition = expectPosition;
	}
	public String getExpectSalary() {
		return expectSalary;
	}
	public void setExpectSalary(String expectSalary) {
		this.expectSalary = expectSalary;
	}
	
}
